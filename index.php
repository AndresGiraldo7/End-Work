    <h1>1. Introduccion</h1>
    <h3>Proyecto desarrollado para almacenes de tipo <strong>Hipermercado en este caso Jumbo Cencosud superficies superiores a los 2500 metros cuadrados o Supermercados menos o igual a 2.500mts. </strong>En este tipo de establecimientos los consumidores pueden ir escogiendo los productos que desean, y los van incluyendo en sus carros de la compra. Al finalizar, hay un servicio de cajas donde pueden pagar la compra que se ha realizado.<br><br>Cuenta con la participacion de diferentes marcas, ademas de su amplio portafolio de marcas propias dando como resultado una gran cantidad de productos</h3><br><br>
    <hr><br>
    <h1>2. Planteamiento del problema</h1><br>
    <h3>La necesidad que se pretende solucionar con el presente proyecto es minimizar los valores porcentuales en las mermas dentro de la cadena de recepción, almacenaje, distribución y posterior venta, especialmente en las mermas por vencimiento, a través de un sistema de alertas tempranas tipo semáforo.Siendo esta, una herramienta útil de control en los inventarios, minimizando las pérdidas que pueden llegar a afectar directamente la rentabilidad de la compañía.<br><br> 
        El origen donde se pueden provocar el aumento de las mermas a lo largo de los procesos radica en lo siguiente:<br><br>

        • No hay suficiente tiempo para hacer seguimiento continuo a la fecha de vencimiento en lo productos.<br>
        • Alto volumen de los stocks que impiden un control unitario de los productos.<br>
        • El llevar el registro manual de las fechas de vencimientos de los productos pueden llegar a no ser exacto debido a errores o fallas humanas no intencionadas.<br>
        • Después del estudio realizado (entrevistas) se detectó que el nivel de porcentaje de merma por vencimiento es de 3,8.<br><br>


        Con lo anteriormente planteado, se evidencia la urgente necesidad de diseñar un aplicativo web para el control de mermas en los inventarios en la empresa CENCOSUD, con un software muy funcional, el cual contribuya al mejoramiento de los procesos. El desarrollo de dicho sistema generaría fidelidad y satisfacción en los clientes al saber que están comprando productos de calidad y no vencidos.
    </h3><br><br><hr><br>
    <h1>3. Justificacion del Proyecto</h1>
    <br><h3>La problemática de la organización consiste en que no cuentan con un sistema adecuado para llevar el control de mermas por vencimiento desde el ingreso y salidas de mercancías de bodega hasta las salas de ventas, ya que este procedimiento se hace de forma manual los que origina varios inconvenientes como: <br><br>•	No se tienen datos actualizados de la existencia de productos próximos a vencer en bodega como en la sala de ventas.<br>• No se puede hacer seguimiento continuo aquellos productos que tienen fecha de vencimiento de manera oportuna ocasionando pérdidas económicas para el Supermercado.<br>•	No se genera reportes oportunos a partir del ingreso de la mercancía al área de recibo lo cual no hay una trazabilidad de la fecha de vencimiento.<br><br>Por lo anterior existe la necesidad de desarrollar un software (Sistema de información) que genere alertar tempranas tipo semáforo que permita controlar las mermas por vencimiento, nace de la necesidad de tener un mayor control de estas dentro de la cadena de recepción, almacenamiento, distribución y posterior venta.<br><br>Ventajas de Desarrollar un software de alertas tipo semáforo para el control de inventarios en la empresa CENCOSUD <br><br>• Permite un mayor control de las mermas mediante la retroalimentación del sistema.<br>• Genera un mayor volumen de ventas a partir de la liquidación de los productos próximos a vencer, a través de grandes ofertas y descuentos.<br>•  Representa mayor satisfacción y fidelidad en el cliente, teniendo en cuenta los productos que va a adquirir son de calidad y no están vencidos ni cercanos a vencer. De esta manera aumentamos el nivel de satisfaccion del cliente respecto a la calidad del producto.<br>• Reduce costos y tiempos en el almacén, al disminuir el porcentaje de perdida de los productos en merma por vencimiento. </h3><br><br><hr><br>
    <h1>4. Objetivo General.</h1><br>
    <h3>Diseñar y Desarrollar un sistema de alertas tempranas tipo semáforo para el control de las mermas por vencimiento en los inventarios de la empresa almacenes jumbo- Cencosud</h3><br>
    <h1>5. Objetivos Especificos</h1>
    <h3>•	Analizar y cuantificar la información recolectada (entrevistas, encuestas, informe generado).<br>•	Diseñar y construir una base de datos con la información requerida para controlar los vencimientos en los productos.<br>•	Realizar las pruebas pertinentes para su desarrollo y su correcto funcionamiento. <br>•	Diseñar el modelo de un software que genere alertas para el control de mermas.<br>•	Desarrollar un sistema único de alerta que sea visible para cada uno de los participantes del proceso.</h3><br><br><hr><br>
